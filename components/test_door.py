from components.Door import Door

class TestDoor:
    self.door = Door()

    def test():
        i = 0
        while True:
            if (self.door.isClosed()):
                self.door.open()
            elif (self.door.isOpen()):
                self.door.close()
            i += 1
            if (i > 2):
                break

if __name__ == "__main__":
    TestDoor.test()